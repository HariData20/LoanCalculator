n1 = input()
n2 = input()
if n1 == '1/2' and n2 == '-1/3':
    print('Strange Quark ')
elif n1 == '1/2' and n2 == '2/3':
    print('Charm Quark')
elif n1 == '1/2' and n2 == '-1':
    print('Electron Lepton')
elif n1 == '1/2' and n2 == '0':
    print('Neutrino Lepton')
elif n1 == '1' and n2 == '0':
    print('Photon Boson')